#include "ofshash.h"
#include "sha1.h"
#include "base64.h"

string ofs_hash(string str) {
	unsigned char sha1hash[20];
	string hash;
	sha1((unsigned char *)str.c_str(), str.length(), sha1hash);
	hash = base64_encode(sha1hash, 20);
	hash = hash.substr(0, hash.length() - 1);
	return hash;
}
